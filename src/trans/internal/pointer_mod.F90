MODULE POINTER_MOD

USE PARKIND1, ONLY : JPRB, JPIM


INTEGER, PARAMETER :: &
    & NPTR_VOR  = B'00000000000000000000000000000001', &
    & NPTR_DIV  = B'00000000000000000000000000000010', &
    & NPTR_U    = B'00000000000000000000000000000100', &
    & NPTR_V    = B'00000000000000000000000000001000', &
    & NPTR_S    = B'00000000000000000000000000010000', &
    & NPTR_EW   = B'00000000000000000000000000100000', &
    & NPTR_NS   = B'00000000000000000000000001000000', &
    & NPTR_U_EW = IOR (NPTR_U, NPTR_EW), &
    & NPTR_V_EW = IOR (NPTR_V, NPTR_EW), &
    & NPTR_S_EW = IOR (NPTR_S, NPTR_EW), &
    & NPTR_S_NS = IOR (NPTR_S, NPTR_NS)

TYPE PTR1D
  REAL (KIND=JPRB), POINTER :: P (:) => NULL ()
  INTEGER (KIND=JPIM) :: KIND = 0, IVSET = 0
END TYPE

TYPE PTR2D
  REAL (KIND=JPRB), POINTER :: P (:,:) => NULL ()
  INTEGER (KIND=JPIM) :: KIND = 0, IVSET = 0
END TYPE

TYPE PTR3D
  REAL (KIND=JPRB), POINTER :: P (:,:,:) => NULL ()
  INTEGER (KIND=JPIM) :: KIND = 0, IVSET = 0
END TYPE

END MODULE
